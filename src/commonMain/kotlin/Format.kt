private const val ESC_RESET = "\u001B[0m"
private const val ESC_BOLD = "\u001B[1m"
private const val ESC_DIM = "\u001B[2m"
private const val ESC_BLACK = "\u001B[30m"
private const val ESC_RED = "\u001B[31m"
private const val ESC_GREEN = "\u001B[32m"
private const val ESC_YELLOW = "\u001B[33m"
private const val ESC_BLUE = "\u001B[34m"
private const val ESC_MAGENTA = "\u001B[35m"
private const val ESC_CYAN = "\u001B[36m"
private const val ESC_WHITE = "\u001B[37m"

fun String.bold() = "$ESC_BOLD$this$ESC_RESET"
fun String.dim() = "$ESC_DIM$this$ESC_RESET"
fun String.red() = "$ESC_RED$this$ESC_RESET"
fun String.cyan() = "$ESC_CYAN$this$ESC_RESET"
fun String.blue() = "$ESC_BLUE$this$ESC_RESET"
fun String.black() = "$ESC_BLACK$this$ESC_RESET"
fun String.green() = "$ESC_GREEN$this$ESC_RESET"
fun String.white() = "$ESC_WHITE$this$ESC_RESET"
fun String.yellow() = "$ESC_YELLOW$this$ESC_RESET"
fun String.magenta() = "$ESC_MAGENTA$this$ESC_RESET"
